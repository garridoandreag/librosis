    private function generateIsbn13()
    {
        $isbn = '';
        for ($i = 0; $i < 12; $i++) {
            $isbn .= mt_rand(0, 9);
        }

        // Calculate check digit
        $sum = 0;
        for ($i = 0; $i < 12; $i++) {
            $sum += ($i % 2 === 0 ? 1 : 3) * $isbn[$i];
        }
        $checkDigit = (10 - ($sum % 10)) % 10;

        return $isbn . $checkDigit;
    }
